public class PeopleDataObject {
    int id;
    String first_name;
    String last_name;
    String email;
    String country;
    String company_name;
    long credit_card;

    public PeopleDataObject (){

    }

    public int getId() {
        return id;
    }

    public String getFirstName() {
        return first_name;
    }

    public String getLastName() {
        return last_name;
    }

    public String getEmail() {
        return email;
    }

    public String getCountry() {
        return country;
    }

    public String getCompanyName() {
        return company_name;
    }

    public long getCreditCard() {
        return credit_card;
    }
}



